const voltageRange = (min?: number, max?: number, unit: "VAC" | "VDC" = "VAC") => {
  if (!min && !max) return undefined;
  if (min && max) return `${min}–${max} ${unit}`;
  if (min) return `${min} ${unit}`;
  if (max) return `${max} ${unit}`;
  return undefined;
};

